import random
import time
from actors import Enemy

def generate_enemy():
  enemies = [
    Enemy("Bokoblin", 5, 2, bokoblin),
    Enemy("Lizalfos", 10, 3, lizalfos),
  ]
  return random.choice(enemies)

def text_printer(text, delay = 0.02, color = None):
  if color:
    text = f"\033[{color}m{text}\033[0m" 
  for char in text:
    print(char, end='', flush=True)
    time.sleep(delay)
  print()

def random_encounter():
  random_number = random.random()
  has_encountered_enemy = random_number <= 0.1
  return has_encountered_enemy

